{

    int n, s, t;
    innn(n, s, t);
    vip(v, n);
    bool same = true;

    for (int i = 1; i <= n; i++)
    {
        if (v[i - 1] != i)
        {
            same = false;
            break;
        }
    }
    if (same || s == t)
    {
        if (s == t)
            prn(0);
        else
            prn(-1);
    }
    else
    {
        int x = s, shuffling = 0, c = 0;
        unordered_map<int, int> m;
        if (v[x - 1] == t)
            c = 1;
        while (v[x - 1] != t)
        {
            if (m[v[x - 1]] != 0)
            {
                shuffling = -1;
                c = 0;
                break;
            }
            m[v[x - 1]]++;
            shuffling++;
            x = v[x - 1];
            c = 1;
        }
        if (c)
            shuffling++;
        prn(shuffling);
    }
}

